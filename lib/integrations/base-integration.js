const chalk = require('chalk');
const fs = require('fs-extra');
const path = require('path');

const MARKER = '<!-- Generated by Vibe Kit -->';
const MARKER_END = '<!-- End Vibe Kit -->';

class BaseIntegration {
  constructor() {
    if (new.target === BaseIntegration) {
      throw new Error('BaseIntegration is abstract and cannot be instantiated directly');
    }
    this.name = '';
    this.displayName = '';
    this.generatedFiles = [];
    this.bridgeFiles = [];
    this.platformDir = '';
  }

  async install() {
    if (this.platformDir) {
      await fs.ensureDir(this.platformDir);
    }
    await this.generateFiles();
  }

  async generateFiles() {
    throw new Error('generateFiles() must be implemented by subclass');
  }

  async validate() {
    const results = { valid: true, missing: [], present: [] };

    const allFiles = [...this.generatedFiles, ...this.bridgeFiles];
    for (const file of allFiles) {
      if (await fs.pathExists(file)) {
        results.present.push(file);
      } else {
        results.missing.push(file);
        results.valid = false;
      }
    }

    return results;
  }

  async writeBridgeFile(filePath, content) {
    const dir = path.dirname(filePath);
    if (dir !== '.') {
      await fs.ensureDir(dir);
    }

    const markedContent = `${MARKER}\n${content}\n${MARKER_END}`;

    if (await fs.pathExists(filePath)) {
      const existing = await fs.readFile(filePath, 'utf-8');

      if (existing.includes(MARKER)) {
        // Replace existing vibe-kit section
        const before = existing.substring(0, existing.indexOf(MARKER));
        const afterMarkerEnd = existing.indexOf(MARKER_END);
        const after = afterMarkerEnd !== -1
          ? existing.substring(afterMarkerEnd + MARKER_END.length)
          : '';
        await fs.writeFile(filePath, before + markedContent + after);
      } else {
        // Append below existing content
        await fs.writeFile(filePath, existing.trimEnd() + '\n\n' + markedContent + '\n');
      }
    } else {
      await fs.writeFile(filePath, markedContent + '\n');
    }
  }

  async writeGeneratedFile(filePath, content) {
    const dir = path.dirname(filePath);
    if (dir !== '.') {
      await fs.ensureDir(dir);
    }
    await fs.writeFile(filePath, content);
  }

  getStandardsBlock() {
    return `## Project Standards

The following standards files define this project's conventions:

- \`.vibe-kit/standards/code-style.md\` — Coding conventions and style rules
- \`.vibe-kit/standards/testing.md\` — Testing patterns and requirements
- \`.vibe-kit/standards/architecture.md\` — Architecture decisions and patterns
- \`.vibe-kit/standards/ai-guidelines.md\` — AI behavior and usage guidelines
- \`.vibe-kit/standards/workflows.md\` — Development workflows and processes
- \`.vibe-kit/standards/glossary.md\` — Project terminology and shortcuts

## Product Context

- \`.vibe-kit/product/mission-lite.md\` — Product mission (condensed)
- \`.vibe-kit/product/decisions.md\` — Architecture Decision Records
- \`.vibe-kit/product/roadmap.md\` — Development roadmap

## Commands

- \`.vibe-kit/commands/analyze.md\` — Analyze and customize standards
- \`.vibe-kit/commands/create-component.md\` — Create components
- \`.vibe-kit/commands/create-feature.md\` — Create features
- \`.vibe-kit/commands/run-tests.md\` — Run tests
- \`.vibe-kit/commands/quality-check.md\` — Quality checks`;
  }

  showUsage() {
    console.log('');
    console.log(chalk.dim(`  See .vibe-kit/standards/ for project standards`));
  }
}

module.exports = BaseIntegration;
